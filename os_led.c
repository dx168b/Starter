//

#include <avr/io.h>

#include "os_led.h"

//////////////////////////////////////////////////////////////////////////////////////////////
void LED_Init(void)
{
	PORTB &= ~LED_MASK;
	DDRB |= LED_MASK;
}

//////////////////////////////////////////////////////////////////////////////////////////////
void LED_On(uint8_t led)
{
	PORTB |= (led & LED_MASK);
}

//////////////////////////////////////////////////////////////////////////////////////////////
void LED_Off(uint8_t led)
{
	PORTB &= ~(led & LED_MASK);
}

//////////////////////////////////////////////////////////////////////////////////////////////
void LED_Toggle(uint8_t led)
{
	PORTB ^= (led & LED_MASK);
}
