#include <avr/io.h>

#include "os_out.h"

//////////////////////////////////////////////////////////////////////////////////////////////
void OUT_Init(void)
{
	PORTB &= ~OUT_MASK;
	DDRB |= OUT_MASK;
}

//////////////////////////////////////////////////////////////////////////////////////////////
void OUT_On(uint8_t out)
{
	PORTB |= (out & OUT_MASK);
}

//////////////////////////////////////////////////////////////////////////////////////////////
void OUT_Off(uint8_t out)
{
	PORTB &= ~(out & OUT_MASK);
}

//////////////////////////////////////////////////////////////////////////////////////////////
void OUT_Toggle(uint8_t out)
{
	PORTB ^= (out & OUT_MASK);
}
